package bitcamp.java100;

public class Test15_7 {

    public static void main(String[] args) {
        System.out.printf("10 == 20 ? %b\n",  10  == 20);
        System.out.printf("10 != 20 ? %b\n",  10  != 20);
        System.out.printf("10 < 20 ? %b\n",  10  < 20);
        System.out.printf("10 <= 20 ? %b\n",  10  <= 20);
        System.out.printf("10 > 20 ? %b\n",  10  > 20);
        System.out.printf("10 >= 20 ? %b\n",  10  >= 20);
    }
}