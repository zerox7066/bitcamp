// ## 연산자 - 관계 연산자(==, !=, <, <=, >, >=)
// - 관계 연산자의 사용법을 알아보자!
//

package bitcamp.java100.ch02;

public class Test15_7 {

    public static void main(String[] args) {
        System.out.printf("10 == 20 ? %b\n", 10 == 20);
        System.out.printf("10 != 20 ? %b\n", 10 != 20);
        System.out.printf("10 < 20 ? %b\n", 10 < 20);
        System.out.printf("10 <= 20 ? %b\n", 10 <= 20);
        System.out.printf("10 > 20 ? %b\n", 10 > 20);
        System.out.printf("10 >= 20 ? %b\n", 10 >= 20);
    }
}
